            const { error: userError } = await supabase
                .from('users')
                .insert([{
                    id: data.user.id,
                    email: email,
                    balance: 10000,
                    role: 'user',
                    created_at: new Date().toISOString()
                }]);
            
            if (userError) throw userError;
            
            // –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —É—Å–ø–µ—à–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
