        const { data, error } = await supabase.auth.signUp({
            email: email,
            password: password,
            options: {
                data: {
                    balance: 10000,
                    role: 'user',
                    created_at: new Date().toISOString()
                }
            }
        });
        
        if (error) throw error;
        
        // –ï—Å–ª–∏ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏—è —É—Å–ø–µ—à–Ω–∞
